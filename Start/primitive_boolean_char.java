package com.Start;

public class primitive_boolean_char {

	public static void main(String[] args) {
		
		boolean var = true;
		System.out.println(var);
		
		char c = 'a';
		System.out.println(c);
		
		char c1 = 98;
		System.out.println(c1);
		
		char c2 = '\u00A7';
		System.out.println(c2);
	}
	
}
