package com.Start;

public class TypeCasting {
	public static void main(String[] args) {
		
		int y = 145;
		
		byte b = (byte)y;
		System.out.println(b);
		
		short s = (short)y;
		System.out.println(s);
		
		float f = (float)y;
		System.out.println(f);
		
		float x = 2.89f;
		int z = (int)x;
		System.out.println(z);
		
		double d = x;
		System.out.println(d);
		
	}
}
