package com.Decision_Making;

public class Assignment_Operators {
	public static void main(String[] args) {
		
		int x = 5;
		x += 5;
		System.out.println(x);
		
		x -= 5;
		System.out.println(x);
		
		x *= 5;
		System.out.println(x);
		
		x /= 5;
		System.out.println(x);
		
		x %= 5;
		System.out.println(x);
	}
}
