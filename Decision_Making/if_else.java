package com.Decision_Making;

public class if_else {
	public static void main(String[] args) {
		
		int x = 5;
		int y = 10;
		
		if (x == y) {
			System.out.println("Condition is True1");
		} else if(x < y) {
			System.out.println("Condition is True2");
		} else 
			System.out.println("Condition is False");
		
	}
}
