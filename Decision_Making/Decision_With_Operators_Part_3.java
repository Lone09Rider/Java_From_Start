package com.Decision_Making;

public class Decision_With_Operators_Part_3 {
	public static void main(String[] args) {
		boolean x = true;
		boolean y = false;
		
		if (x && y) {
			System.out.println("Condition False");
		}
		
		if (x || y) {
			System.out.println("Condition True");
		}
		
		if (x && x) {
			System.out.println("Condition True");
		}
		
		else 
			System.out.println("Condition False");
		
	}
}
