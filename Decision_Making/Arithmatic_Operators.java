package com.Decision_Making;

public class Arithmatic_Operators {

	public static void main(String[] args) {
		
		double x = 1;
		double y = 2;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println(y++);
		System.out.println(y);
		System.out.println(++y);
		System.out.println(x--);
		System.out.println(x);
		System.out.println(--x);
	}
	
}
