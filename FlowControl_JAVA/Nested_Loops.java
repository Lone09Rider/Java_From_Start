package com.FlowControl_JAVA;

public class Nested_Loops {

	public static void main(String[] args) {
		
		for (int i = 1; i <= 10; i++)
		{
			for (int j = 1; j <= 3; j++)
			{
				System.out.println("In J");
			}
			System.out.println("In I");
			System.out.println("____________");
		}
		
	}

}
